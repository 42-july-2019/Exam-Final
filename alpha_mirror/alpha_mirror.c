#include <stdio.h>

#include <unistd.h>

int		main(int argc, char **argv)
{
	char c;

	if (argc == 2)
	{
		while (*argv[1])
		{
			c = *argv[1];
			if (c >= 'a' && c <= 'z')
				c = 'z' - c + 'a';
			else if (c >= 'A' && c <= 'Z')
				c = 'Z' - c + 'A';
			write(1, &c, 1);
			argv[1]++;
		}
	}
	write(1, "\n", 1);
}
